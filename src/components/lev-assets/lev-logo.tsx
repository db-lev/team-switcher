/**
 * Lev logo in three variants:
 *
 * - `icon` — the Lev mark only
 * - `logo` — the "Lev" wordmark only
 * - `lockup` — wordmark + icon side by side
 *
 * Usage:
 *   <LevLogo variant="icon" className="size-6" />
 *   <LevLogo variant="logo" className="h-6 w-auto" />
 *   <LevLogo variant="lockup" className="h-8 w-auto" />
 */
export function LevLogo({
  variant = "icon",
  className,
  ...props
}: SVGProps<SVGSVGElement> & {
  variant?: "icon" | "logo" | "lockup"
}) {
  if (variant === "icon") {
    return <LevIcon className={className} {...props} />
  }

  if (variant === "logo") {
    return <LevWordmark className={className} {...props} />
  }

  return <LevLockup className={className} {...props} />
}

